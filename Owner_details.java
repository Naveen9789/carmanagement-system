    // Automatic id generation to avoid the duplications in primarykey 
    private void Autoid() {
        String query = "select max(ownerid) from ownerdetails";
        try {
            pst = con.prepareStatement(query);
            rs = pst.executeQuery();
            rs.next();
            String maxcarid = rs.getString(1);

            if (maxcarid == null) {
                ownerid_txt.setText("OD001"); //OD represents ownerdetails
            } else {
                int id = Integer.parseInt(maxcarid.substring(2)) + 1;
                ownerid_txt.setText("OD" + String.format("%03d", id));
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error while generation of autoid");
            System.out.println(ex);
        }
    }

    //To insert the values in the cardetails table
    private void insert() {
        long phoneno = Long.parseLong(phonenumber);
        int ownerprice = Integer.parseInt(price);
        String query = "insert into ownerdetails values(?,?,?,?,?)";
        try {
            pst = con.prepareStatement(query);
            pst.setString(1, ownerid);
            pst.setString(2, name);
            pst.setLong(3, phoneno);
            pst.setString(4, address);
            pst.setInt(5, ownerprice);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "inserted successfully");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error while insertion of data");
            System.out.println(ex);
        }
    }

