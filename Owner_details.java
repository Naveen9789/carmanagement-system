    //To load the ownerdetail in the table
    private void ownerdetail_table() {
        try {
            pst = con.prepareStatement("select * from ownerdetails");
            rs = pst.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            int c = rsmd.getColumnCount();

            DefaultTableModel df = (DefaultTableModel) ownerdetails_table.getModel();
            df.setRowCount(0);

            while (rs.next()) {
                ArrayList<String> rowdata = new ArrayList<>();
                for (int i = 1; i <= c; i++) {
                    rowdata.add(rs.getString(i));
                }
                df.addRow(rowdata.toArray());
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error while loading the ownerdetails table");
            System.out.println(ex);

        }
    }

    //To update the values in the cardetails table
    private void update() {
        try {
            String query = "update ownerdetails set name=?,phonenumber=?,address=?,price=? where ownerid=?";
            pst = con.prepareStatement(query);
            pst.setString(1, name);
            pst.setString(2, phonenumber);
            pst.setString(3, address);
            pst.setString(4, price);
            pst.setString(5, ownerid);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data updated successfully");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error while updation");
            System.out.println(ex);
        }
    }

